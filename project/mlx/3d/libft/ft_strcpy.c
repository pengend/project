#include "libft.h"

char *ft_strcpy(char *dest, const char *src)
{
	int i;

	i = 0;
	while (dest && src && src[i])
	{
		dest[i] = src[i];
		i++;
	}
	if (i > 0)
		dest[i] = '\0';
	return (dest);
}
