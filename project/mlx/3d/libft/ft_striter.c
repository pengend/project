#include "libft.h"

void ft_striter(char *s, void (*f)(char*))
{
	unsigned int l;
	unsigned int i;

	if (!s || !f)
		return ;
	i = 0;
	l = ft_strlen(s);
	while (i < l)
	{
		(*f)(&(s[i]));
		i++;
	}
}
