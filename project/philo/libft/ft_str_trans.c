#include "libft.h"

char		*ft_str_trans(char *s)
{
	char	*t;
	int		i;

	t = ft_strnew(ft_strlen(s));
	i = 0;
	while (*s)
	{
		if (*s == '^' && *(s + 1) >= 64 && *(s + 1) <= 95)
		{
			t[i] = *(s + 1) - 64;
			s += 2;
			i++;
			continue;
		}
		t[i] = *s;
		i++;
		s++;
	}
	return (t);
}
