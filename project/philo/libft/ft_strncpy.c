#include "libft.h"

char *ft_strncpy(char *dest, const char *src, size_t n)
{
	unsigned int i;
	unsigned int len;

	i = 0;
	len = ft_strlen(src);
	while (i < n && i <= len)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
