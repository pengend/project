#include "libft.h"

void ft_tabcpy(char ***dest, char **src)
{
	int i;

	i = 0;
	while (src[i])
	{
		(*dest)[i] = ft_strdup(src[i]);
		i++;
	}
	(*dest)[i] = NULL;
}
